- (IBAction)unwindToTankList:(UIStoryboardSegue *)segue
{
    acAddFishTankViewController *source = [segue sourceViewController];
    acFishTank *fishTank = source.fishTank;
    if(fishTank !=nil)
    {
        [data.fishTanks addObject:fishTank];
        [self.tableView reloadData];
    }
}

#pragma mark - Table View

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [data.fishTanks count];
//    return _objects.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"ListPrototypeCell" forIndexPath:indexPath];

    acFishTank *fishTank = [data.fishTanks objectAtIndex:indexPath.row];
    cell.textLabel.text = fishTank.name;
    return cell;
}

